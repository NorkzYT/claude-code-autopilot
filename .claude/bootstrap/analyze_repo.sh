build_repo_snapshot() {
  local workspace="$1"
  python3 - "$workspace" <<'PY'
import os
import subprocess
import sys
from pathlib import Path

root = Path(sys.argv[1])
sections = []

# ── 1. Directory tree (500 lines) ──
try:
    tree_out = subprocess.check_output(
        ["tree", "-L", "3", "--dirsfirst", "-I",
         "node_modules|.git|.next|.cache|.turbo|dist|build|__pycache__|.venv|venv|target|vendor|.idea|.vscode|coverage"],
        cwd=str(root), stderr=subprocess.DEVNULL, text=True, timeout=15
    )
except Exception:
    # Fallback: find-based listing
    try:
        tree_out = subprocess.check_output(
            ["find", ".", "-maxdepth", "3", "-not", "-path", "*/.git/*",
             "-not", "-path", "*/node_modules/*", "-not", "-path", "*/__pycache__/*",
             "-not", "-path", "*/.next/*", "-not", "-path", "*/.cache/*"],
            cwd=str(root), stderr=subprocess.DEVNULL, text=True, timeout=15
        )
    except Exception:
        tree_out = "(tree unavailable)"

tree_lines = tree_out.strip().splitlines()[:500]
sections.append("## Directory Structure\n```\n" + "\n".join(tree_lines) + "\n```")

# ── 2. Root config files (first 100 lines each, max 10) ──
config_patterns = [
    "package.json", "tsconfig.json", "pyproject.toml", "go.mod", "Cargo.toml",
    "docker-compose.yml", "docker-compose.yaml", "compose.yml", "compose.yaml",
    "turbo.json", "nx.json", "lerna.json", "pnpm-workspace.yaml",
    "Makefile", "Dockerfile", "requirements.txt", "setup.py", "setup.cfg",
    ".eslintrc.json", ".eslintrc.js", "biome.json", "deno.json"
]
config_found = 0
config_parts = []
for name in config_patterns:
    if config_found >= 10:
        break
    p = root / name
    if p.is_file():
        try:
            lines = p.read_text(errors="replace").splitlines()[:100]
            config_parts.append(f"### {name}\n```\n" + "\n".join(lines) + "\n```")
            config_found += 1
        except Exception:
            pass
if config_parts:
    sections.append("## Root Config Files\n" + "\n\n".join(config_parts))

# ── 3. CI workflows (first 60 lines each, max 5) ──
ci_dir = root / ".github" / "workflows"
ci_parts = []
if ci_dir.is_dir():
    ci_files = sorted(ci_dir.glob("*.yml")) + sorted(ci_dir.glob("*.yaml"))
    for cf in ci_files[:5]:
        try:
            lines = cf.read_text(errors="replace").splitlines()[:60]
            ci_parts.append(f"### {cf.name}\n```yaml\n" + "\n".join(lines) + "\n```")
        except Exception:
            pass
if ci_parts:
    sections.append("## CI Workflows\n" + "\n\n".join(ci_parts))

# ── 4. README.md (first 200 lines) ──
readme = root / "README.md"
if readme.is_file():
    try:
        lines = readme.read_text(errors="replace").splitlines()[:200]
        sections.append("## README.md\n" + "\n".join(lines))
    except Exception:
        pass

# ── 5. Top-level entrypoints (first 80 lines each, max 8) ──
entry_globs = [
    "src/index.*", "src/main.*", "src/app.*",
    "app/main.*", "app/index.*", "app.py", "main.py", "server.py",
    "cmd/main.go", "cmd/*/main.go",
    "lib/index.*", "lib/main.*",
    "index.ts", "index.js", "main.ts", "main.js",
    "src/lib.rs", "src/main.rs",
]
entry_found = 0
entry_parts = []
for pat in entry_globs:
    if entry_found >= 8:
        break
    for match in sorted(root.glob(pat)):
        if entry_found >= 8:
            break
        if not match.is_file():
            continue
        try:
            lines = match.read_text(errors="replace").splitlines()[:80]
            relpath = match.relative_to(root)
            entry_parts.append(f"### {relpath}\n```\n" + "\n".join(lines) + "\n```")
            entry_found += 1
        except Exception:
            pass
if entry_parts:
    sections.append("## Top-Level Entrypoints\n" + "\n\n".join(entry_parts))

snapshot = "\n\n".join(sections)
print(snapshot)
PY
}

pick_deep_scan_timeouts() {
  local workspace="$1"
  local file_count total_bytes size_mb tier timeout_s retry_s source

  # Respect explicit overrides.
  if [[ -n "${CLAUDE_DEEP_TIMEOUT:-}" || -n "${CLAUDE_DEEP_RETRY_TIMEOUT:-}" ]]; then
    timeout_s="${CLAUDE_DEEP_TIMEOUT:-420}"
    retry_s="${CLAUDE_DEEP_RETRY_TIMEOUT:-900}"
    echo "override|unknown|unknown|${timeout_s}|${retry_s}|override"
    return 0
  fi

  IFS='|' read -r file_count total_bytes source <<<"$(estimate_repo_deep_scan_size "$workspace" 2>/dev/null || echo '0|0|unknown')"
  file_count="${file_count:-0}"
  total_bytes="${total_bytes:-0}"
  source="${source:-unknown}"
  size_mb=$(( total_bytes / 1024 / 1024 ))

  if (( file_count <= 800 && size_mb <= 10 )); then
    tier="small"; timeout_s=300; retry_s=600
  elif (( file_count <= 4000 && size_mb <= 60 )); then
    tier="medium"; timeout_s=600; retry_s=1200
  elif (( file_count <= 15000 && size_mb <= 250 )); then
    tier="large"; timeout_s=1200; retry_s=2400
  else
    tier="xlarge"; timeout_s=1800; retry_s=3600
  fi

  echo "${tier}|${file_count}|${size_mb}|${timeout_s}|${retry_s}|${source}"
}

# ─── Parse Arguments ────────────────────────────────────────
WORKSPACE="${1:-}"
DEEP=false
DEBUG=false

if [[ -z "$WORKSPACE" ]]; then
  echo "Usage: bash $0 <workspace-path> [--deep]" >&2
  exit 1
fi

shift
while [[ $# -gt 0 ]]; do
  case "$1" in
    --deep) DEEP=true; shift ;;
    --debug) DEBUG=true; shift ;;
    *)      warn "Unknown option: $1"; shift ;;
  esac
done

if [[ ! -d "$WORKSPACE" ]]; then
  echo "ERROR: Workspace directory does not exist: $WORKSPACE" >&2
  exit 1
fi

WORKSPACE="$(cd "$WORKSPACE" && pwd)"
OPENCLAW_DIR="$WORKSPACE/.openclaw"
mkdir -p "$OPENCLAW_DIR"

# ─── Extract project name ───────────────────────────────────
PROJECT_NAME=""
PROJECT_DESC=""

# Try package.json
if [[ -f "$WORKSPACE/package.json" ]]; then
  PROJECT_NAME="$(python3 -c "
import json
with open('$WORKSPACE/package.json') as f:
    d = json.load(f)
print(d.get('name', ''))
" 2>/dev/null || echo "")"
  PROJECT_DESC="$(python3 -c "
import json
with open('$WORKSPACE/package.json') as f:
    d = json.load(f)
print(d.get('description', ''))
" 2>/dev/null || echo "")"
fi

# Try pyproject.toml
if [[ -z "$PROJECT_NAME" ]] && [[ -f "$WORKSPACE/pyproject.toml" ]]; then
  PROJECT_NAME="$(python3 -c "
import re
with open('$WORKSPACE/pyproject.toml') as f:
    content = f.read()
m = re.search(r'^name\s*=\s*\"([^\"]+)\"', content, re.MULTILINE)
print(m.group(1) if m else '')
" 2>/dev/null || echo "")"
fi

# Try go.mod
if [[ -z "$PROJECT_NAME" ]] && [[ -f "$WORKSPACE/go.mod" ]]; then
  PROJECT_NAME="$(head -1 "$WORKSPACE/go.mod" 2>/dev/null | awk '{print $2}' | awk -F/ '{print $NF}' || echo "")"
fi

# Try Cargo.toml
if [[ -z "$PROJECT_NAME" ]] && [[ -f "$WORKSPACE/Cargo.toml" ]]; then
  PROJECT_NAME="$(python3 -c "
import re
with open('$WORKSPACE/Cargo.toml') as f:
    content = f.read()
m = re.search(r'^name\s*=\s*\"([^\"]+)\"', content, re.MULTILINE)
print(m.group(1) if m else '')
" 2>/dev/null || echo "")"
fi

# Fallback to directory name
if [[ -z "$PROJECT_NAME" ]]; then
  PROJECT_NAME="$(basename "$WORKSPACE")"
fi

log "Analyzing project: $PROJECT_NAME"

# ═══════════════════════════════════════════════════════════════
# Phase A: Static Parsing
# ═══════════════════════════════════════════════════════════════

if [[ "$DEEP" == "false" ]]; then
  log "Phase A: Static repo analysis..."

  # ─── Detect commands ────────────────────────────────────────
  BUILD_CMD=""
  TEST_CMD=""
  LINT_CMD=""
  DEV_CMD=""
  START_CMD=""
  TYPECHECK_CMD=""
  RUN_LOCAL_CMD=""
  CONFIRM_CMD=""
  WORKSPACES=""
  SERVICES=""
  PORTS=""
  ENV_VARS=""
  CI_COMMANDS=""
  NODE_PM=""
  NODE_PM_FIELD=""
  MAKE_TARGETS=""

  # --- package.json ---
  if [[ -f "$WORKSPACE/package.json" ]]; then
    log "  Parsing package.json..."
    eval "$(python3 -c "
import json
with open('$WORKSPACE/package.json') as f:
    d = json.load(f)
scripts = d.get('scripts', {})
ws = d.get('workspaces', [])
package_manager = d.get('packageManager', '')
if isinstance(ws, dict):
    ws = ws.get('packages', [])

if package_manager:
    print(f\"NODE_PM_FIELD='{package_manager.split('@', 1)[0]}'\")
for script_name in ('build', 'test', 'lint', 'dev', 'start', 'typecheck', 'type-check'):
    if script_name in scripts:
        shell_key = script_name.upper().replace('-', '_')
        print(f\"HAS_SCRIPT_{shell_key}=1\")
if ws: print(f\"WORKSPACES='{', '.join(ws)}'\")
" 2>/dev/null || echo "")"

    if [[ -f "$WORKSPACE/pnpm-lock.yaml" ]]; then
      NODE_PM="pnpm"
    elif [[ -f "$WORKSPACE/yarn.lock" ]]; then
      NODE_PM="yarn"
    elif [[ -f "$WORKSPACE/bun.lockb" || -f "$WORKSPACE/bun.lock" ]]; then
      NODE_PM="bun"
    elif [[ -f "$WORKSPACE/package-lock.json" ]]; then
      NODE_PM="npm"
    elif [[ -n "$NODE_PM_FIELD" ]]; then
      NODE_PM="$NODE_PM_FIELD"
    else
      NODE_PM="npm"
    fi

    [[ "${HAS_SCRIPT_BUILD:-0}" == "1" ]] && BUILD_CMD="$(node_script_cmd "$NODE_PM" build)"
    [[ "${HAS_SCRIPT_TEST:-0}" == "1" ]] && TEST_CMD="$(node_script_cmd "$NODE_PM" test)"
    [[ "${HAS_SCRIPT_LINT:-0}" == "1" ]] && LINT_CMD="$(node_script_cmd "$NODE_PM" lint)"
    [[ "${HAS_SCRIPT_DEV:-0}" == "1" ]] && DEV_CMD="$(node_script_cmd "$NODE_PM" dev)"
    [[ "${HAS_SCRIPT_START:-0}" == "1" ]] && START_CMD="$(node_script_cmd "$NODE_PM" start)"
    if [[ "${HAS_SCRIPT_TYPECHECK:-0}" == "1" ]]; then
      TYPECHECK_CMD="$(node_script_cmd "$NODE_PM" typecheck)"
    elif [[ "${HAS_SCRIPT_TYPE_CHECK:-0}" == "1" ]]; then
      TYPECHECK_CMD="$(node_script_cmd "$NODE_PM" type-check)"
    fi
  fi

  # --- Makefile ---
  if [[ -f "$WORKSPACE/Makefile" ]]; then
    log "  Parsing Makefile..."
    # Extract phony targets or simple targets
    MAKE_TARGETS="$(grep -E '^[a-zA-Z_-]+:' "$WORKSPACE/Makefile" 2>/dev/null | sed 's/:.*//' | sort -u || echo "")"
    if echo "$MAKE_TARGETS" | grep -q '^build$' && [[ -z "$BUILD_CMD" ]]; then
      BUILD_CMD="make build"
    fi
    if echo "$MAKE_TARGETS" | grep -q '^test$' && [[ -z "$TEST_CMD" ]]; then
      TEST_CMD="make test"
    fi
    if echo "$MAKE_TARGETS" | grep -q '^lint$' && [[ -z "$LINT_CMD" ]]; then
      LINT_CMD="make lint"
    fi
    if echo "$MAKE_TARGETS" | grep -q '^dev$' && [[ -z "$DEV_CMD" ]]; then
      DEV_CMD="make dev"
    fi
    if echo "$MAKE_TARGETS" | grep -q '^typecheck$' && [[ -z "$TYPECHECK_CMD" ]]; then
      TYPECHECK_CMD="make typecheck"
    fi
    if [[ -z "$RUN_LOCAL_CMD" ]]; then
      for tgt in up run dev serve start local-up local_up stack-up stack_up; do
        if echo "$MAKE_TARGETS" | grep -q "^${tgt}$"; then
          RUN_LOCAL_CMD="make $tgt"
          break
        fi
      done
    fi
  fi

  # --- pyproject.toml ---
  if [[ -f "$WORKSPACE/pyproject.toml" ]]; then
    log "  Parsing pyproject.toml..."
    eval "$(python3 -c "
import re
with open('$WORKSPACE/pyproject.toml') as f:
    content = f.read()

# Check for pytest
if '[tool.pytest' in content and not '$TEST_CMD':
    print(\"TEST_CMD='python -m pytest'\")

# Check for ruff or flake8
if '[tool.ruff' in content and not '$LINT_CMD':
    print(\"LINT_CMD='ruff check .'\")
elif '[tool.flake8' in content and not '$LINT_CMD':
    print(\"LINT_CMD='flake8 .'\")

# Check for mypy
if '[tool.mypy' in content and not '$TYPECHECK_CMD':
    print(\"TYPECHECK_CMD='mypy .'\")

# Check build system
m = re.search(r'build-backend\s*=\s*\"([^\"]+)\"', content)
if m and not '$BUILD_CMD':
    backend = m.group(1)
    if 'poetry' in backend:
        print(\"BUILD_CMD='poetry build'\")
    elif 'hatch' in backend:
        print(\"BUILD_CMD='hatch build'\")
    else:
        print(\"BUILD_CMD='python -m build'\")
" 2>/dev/null || echo "")"
  fi

  # --- go.mod ---
  if [[ -f "$WORKSPACE/go.mod" ]] && [[ -z "$BUILD_CMD" ]]; then
    BUILD_CMD="go build ./..."
    [[ -z "$TEST_CMD" ]] && TEST_CMD="go test ./..."
    [[ -z "$LINT_CMD" ]] && LINT_CMD="golangci-lint run"
  fi

  # --- Cargo.toml ---
  if [[ -f "$WORKSPACE/Cargo.toml" ]] && [[ -z "$BUILD_CMD" ]]; then
    BUILD_CMD="cargo build"
    [[ -z "$TEST_CMD" ]] && TEST_CMD="cargo test"
    [[ -z "$LINT_CMD" ]] && LINT_CMD="cargo clippy"
  fi

  # --- docker-compose.yml ---
  for compose_file in "$WORKSPACE/docker-compose.yml" "$WORKSPACE/docker-compose.yaml"; do
    if [[ -f "$compose_file" ]]; then
      log "  Parsing $(basename "$compose_file")..."
      [[ -z "$RUN_LOCAL_CMD" ]] && RUN_LOCAL_CMD="docker compose up -d"
      SERVICES="$(python3 -c "
import re
with open('$compose_file') as f:
    content = f.read()
# Simple extraction of service names and ports
in_services = False
current_service = ''
for line in content.split('\n'):
    stripped = line.strip()
    # Top-level services: key
    if re.match(r'^services:', line):
        in_services = True
        continue
    if in_services and re.match(r'^[a-z]', line) and not line.startswith(' '):
        in_services = False
        continue
    if in_services and re.match(r'^  [a-zA-Z_-]+:', line):
        current_service = stripped.rstrip(':')
    if in_services and current_service and '\"' in stripped:
        m = re.search(r'\"(\d+):(\d+)\"', stripped)
        if m:
            print(f'{current_service}|{m.group(1)}')
" 2>/dev/null || echo "")"
      break
    fi
  done
  if [[ -z "$RUN_LOCAL_CMD" ]] && [[ -f "$WORKSPACE/compose.yml" || -f "$WORKSPACE/compose.yaml" ]]; then
    RUN_LOCAL_CMD="docker compose up -d"
  fi

  # --- .env.example ---
  if [[ -f "$WORKSPACE/.env.example" ]]; then
    log "  Parsing .env.example..."
    ENV_VARS="$(grep -E '^[A-Z_]+=' "$WORKSPACE/.env.example" 2>/dev/null | sed 's/=.*//' | head -20 || echo "")"
  fi

  if [[ -z "$RUN_LOCAL_CMD" ]]; then
    if [[ -n "$DEV_CMD" ]]; then
      RUN_LOCAL_CMD="$DEV_CMD"
    elif [[ -n "$START_CMD" ]]; then
      RUN_LOCAL_CMD="$START_CMD"
    fi
  fi

  FIRST_PORT="$(first_detected_port || true)"
  if [[ -n "$FIRST_PORT" ]]; then
    CONFIRM_CMD="curl -fsS http://127.0.0.1:${FIRST_PORT}/ || curl -fsS http://127.0.0.1:${FIRST_PORT}/health"
  elif [[ -n "$RUN_LOCAL_CMD" ]]; then
    CONFIRM_CMD="# Open the local app and verify the changed flow manually"
  fi

  # --- CI workflows ---
  if [[ -d "$WORKSPACE/.github/workflows" ]]; then
    log "  Parsing CI workflows..."
    CI_COMMANDS="$(python3 -c "
import os, re
wf_dir = '$WORKSPACE/.github/workflows'
commands = set()
for fname in os.listdir(wf_dir):
    if not fname.endswith(('.yml', '.yaml')):
        continue
    with open(os.path.join(wf_dir, fname)) as f:
        for line in f:
            line = line.strip()
            if line.startswith('- run:') or line.startswith('run:'):
                cmd = line.split(':', 1)[1].strip()
                if cmd and not cmd.startswith('|'):
                    commands.add(cmd)
for c in sorted(commands):
    print(c)
" 2>/dev/null || echo "")"
  fi

  # --- README.md quick start ---
  README_SECTION=""
  if [[ -f "$WORKSPACE/README.md" ]]; then
    log "  Parsing README.md..."
    README_SECTION="$(python3 -c "
import re
with open('$WORKSPACE/README.md') as f:
    content = f.read()
# Extract Quick Start / Development / Getting Started sections
sections = []
for pattern in [r'##\s+(?:Quick\s+Start|Getting\s+Started|Development)', r'##\s+Setup']:
    m = re.search(pattern, content, re.IGNORECASE)
    if m:
        start = m.start()
        # Find next ## heading
        end_m = re.search(r'\n## ', content[start+5:])
        end = start + 5 + end_m.start() if end_m else min(start + 1000, len(content))
        sections.append(content[start:end].strip())
if sections:
    print('\n\n'.join(sections[:2]))
" 2>/dev/null || echo "")"
  fi

  # ─── Generate TOOLS.md ──────────────────────────────────────
  TOOLS_FILE="$WORKSPACE/TOOLS.md"

  if is_custom "$TOOLS_FILE"; then
    skip "TOOLS.md has custom content, not overwriting"
  else
    log "  Writing TOOLS.md..."
    {
      echo "$AUTO_GEN_MARKER"
      echo "# TOOLS.md - ${PROJECT_NAME} Commands"
      echo ""
      echo "## Build"
      echo '```'
      echo "${BUILD_CMD:-# No build command detected}"
      echo '```'
      echo ""
      echo "## Test"
      echo '```'
      echo "${TEST_CMD:-# No test command detected}"
      echo '```'
      echo ""
      echo "## Lint"
      echo '```'
      echo "${LINT_CMD:-# No lint command detected}"
      echo '```'
      echo ""
      if [[ -n "$TYPECHECK_CMD" ]]; then
        echo "## Typecheck"
        echo '```'
        echo "$TYPECHECK_CMD"
        echo '```'
        echo ""
      fi
      echo "## Dev Server"
      echo '```'
      echo "${DEV_CMD:-# No dev command detected}"
      echo '```'
      echo ""
      echo "## Run Local Stack (Use this for local 'deploy')"
      echo '```'
      echo "${RUN_LOCAL_CMD:-# No local run command detected (check README / Makefile / docker compose)}"
      echo '```'
      echo ""
      echo "## Confirm (Smoke Check)"
      echo '```'
      echo "${CONFIRM_CMD:-# No confirm command detected (run local app and verify the changed flow)}"
      echo '```'
      echo ""
      if [[ -n "$WORKSPACES" ]]; then
        echo "## Workspaces"
        echo ""
        echo "$WORKSPACES" | tr ',' '\n' | while read -r ws; do
          ws="$(echo "$ws" | xargs)"
          [[ -n "$ws" ]] && echo "- \`$ws\`"
        done
        echo ""
      fi
      echo "## Service Ports"
      echo ""
      echo "| Service | Port |"
      echo "|---------|------|"
      if [[ -n "$SERVICES" ]]; then
        echo "$SERVICES" | while IFS='|' read -r svc port; do
          [[ -n "$svc" ]] && echo "| $svc | $port |"
        done
      else
        echo "| (none detected) | - |"
      fi
      echo ""
      if [[ -n "$ENV_VARS" ]]; then
        echo "## Environment Variables"
        echo ""
        echo "From \`.env.example\`:"
        echo ""
        echo "$ENV_VARS" | while read -r var; do
          [[ -n "$var" ]] && echo "- \`$var\`"
        done
        echo ""
      fi
      echo "## Git Policy"
      echo ""
      echo "- Feature branches: use professional names (e.g., feat/<description>, fix/<description>, chore/<description>)"
      echo "- Commit format: conventional commits (feat:, fix:, chore:)"
      echo "- NEVER include Co-Authored-By lines -- commits must appear as the user's own"
      echo "- Push the branch to origin before creating a PR"
      echo "- Create PR only after build/local-run/test/confirm is complete"
    } > "$TOOLS_FILE"
    log "Created: $TOOLS_FILE"
  fi

  # ─── Generate HEARTBEAT.md ──────────────────────────────────
  HEARTBEAT_FILE="$WORKSPACE/HEARTBEAT.md"

  if is_custom "$HEARTBEAT_FILE"; then
    skip "HEARTBEAT.md has custom content, not overwriting"
  else
    log "  Writing HEARTBEAT.md..."
    {
      echo "$AUTO_GEN_MARKER"
      echo "# HEARTBEAT.md"
      echo ""
      echo "When asked for a heartbeat, check these and report:"
      echo ""
      if [[ -n "$BUILD_CMD" ]]; then
        echo "1. Build succeeds: \`$BUILD_CMD\`"
      else
        echo "1. Build succeeds: (no build command detected)"
      fi
      if [[ -n "$TEST_CMD" ]]; then
        echo "2. Tests pass: \`$TEST_CMD\`"
      else
        echo "2. Tests pass: (no test command detected)"
      fi
      if [[ -n "$LINT_CMD" ]]; then
        echo "3. Lint clean: \`$LINT_CMD\`"
      else
        echo "3. Lint clean: (no lint command detected)"
      fi
      if [[ -n "$RUN_LOCAL_CMD" ]]; then
        echo "4. Local stack starts: \`$RUN_LOCAL_CMD\`"
      else
        echo "4. Local stack starts: (no local run command detected)"
      fi
      if [[ -n "$CONFIRM_CMD" ]]; then
        echo "5. Smoke check passes: \`$CONFIRM_CMD\`"
      else
        echo "5. Smoke check passes: (manual local verification required)"
      fi
      echo "6. Git status: any uncommitted changes?"
    } > "$HEARTBEAT_FILE"
    log "Created: $HEARTBEAT_FILE"
  fi

  log "Phase A complete."
  echo "  Generated files:"
  echo "    - $TOOLS_FILE"
  echo "    - $HEARTBEAT_FILE"
  if [[ ! -s "$WORKSPACE/PROJECT.md" ]]; then
    echo ""
    echo "  Note: PROJECT.md is generated only in deep mode."
    echo "  Run: bash $0 $WORKSPACE --deep"
  fi

# ═══════════════════════════════════════════════════════════════
# Phase B: Deep Scan (Claude-powered)
# ═══════════════════════════════════════════════════════════════

else
  log "Phase B: Deep codebase understanding..."

  PROJECT_FILE="$WORKSPACE/PROJECT.md"
  PROJECT_FILE_PRE_SHA="$(file_sha256 "$PROJECT_FILE" || true)"

  if [[ -s "$PROJECT_FILE" ]]; then
    if is_custom "$PROJECT_FILE"; then
      skip "PROJECT.md has custom content, not overwriting"
      exit 0
    fi
    log "PROJECT.md is auto-generated; refreshing with a new deep scan"
  fi

  if ! has claude; then
    warn "Claude CLI not found — cannot run deep analysis"
    warn "Install Claude Code CLI and re-run: bash $0 $WORKSPACE --deep"
    exit 1
  fi

  IFS='|' read -r DEEP_TIER DEEP_FILE_COUNT DEEP_SIZE_MB CLAUDE_DEEP_TIMEOUT CLAUDE_DEEP_RETRY_TIMEOUT DEEP_SIZE_SOURCE \
    <<<"$(pick_deep_scan_timeouts "$WORKSPACE")"
  DEEP_DISABLE_TIMEOUT=false
  if [[ "${CLAUDE_DEEP_NO_TIMEOUT:-0}" == "1" || "${CLAUDE_DEEP_WAIT_FOR_COMPLETION:-0}" == "1" ]]; then
    DEEP_DISABLE_TIMEOUT=true
  fi

  DEEP_USE_TIMEOUT=false
  if has timeout && [[ "$DEEP_DISABLE_TIMEOUT" == "false" ]]; then
    DEEP_USE_TIMEOUT=true
  fi

  if [[ "$DEEP_USE_TIMEOUT" == "true" ]]; then
    if [[ "$DEEP_TIER" == "override" ]]; then
      log "Running Claude deep scan (timeout: ${CLAUDE_DEEP_TIMEOUT}s, retry: ${CLAUDE_DEEP_RETRY_TIMEOUT}s; env override)..."
    else
      log "Deep scan size estimate: tier=${DEEP_TIER}, files=${DEEP_FILE_COUNT}, size≈${DEEP_SIZE_MB}MB (source=${DEEP_SIZE_SOURCE})"
      log "Running Claude deep scan (timeout: ${CLAUDE_DEEP_TIMEOUT}s, retry: ${CLAUDE_DEEP_RETRY_TIMEOUT}s)..."
    fi
  elif [[ "$DEEP_DISABLE_TIMEOUT" == "true" ]]; then
    log "Running Claude deep scan (no timeout; wait-until-complete mode enabled)"
  else
    log "Running Claude deep scan (no timeout command found; may take several minutes)..."
  fi

  GITIGNORE_CONTEXT="$(gitignore_prompt_context "$WORKSPACE" || true)"
  TOOLLESS_MODE=false
  REPO_SNAPSHOT=""
  SNAPSHOT_SIZE=0
  SHOULD_PRECOMPUTE_SNAPSHOT=false
  SHOULD_DEFAULT_TOOLLESS=false

  if [[ "$DEEP_TIER" == "large" || "$DEEP_TIER" == "xlarge" ]]; then
    SHOULD_PRECOMPUTE_SNAPSHOT=true
    SHOULD_DEFAULT_TOOLLESS=true
  elif (( DEEP_FILE_COUNT <= 1200 )) && (( DEEP_SIZE_MB <= 25 )); then
    # Small/medium repos are often better handled by a bounded snapshot than by
    # letting Claude recursively inspect the tree, which can get killed (rc=137)
    # before it emits any PROJECT.md output.
    SHOULD_PRECOMPUTE_SNAPSHOT=true
    SHOULD_DEFAULT_TOOLLESS=true
  fi

  if [[ "$SHOULD_PRECOMPUTE_SNAPSHOT" == "true" ]]; then
    log "Building pre-computed repo snapshot for deep scan (${DEEP_TIER} repo)..."
    REPO_SNAPSHOT="$(build_repo_snapshot "$WORKSPACE" 2>/dev/null || true)"
    if [[ -n "$REPO_SNAPSHOT" ]]; then
      SNAPSHOT_SIZE="${#REPO_SNAPSHOT}"
      if [[ "$DEBUG" == "true" ]]; then
        echo "    [DEBUG] Snapshot size: ${SNAPSHOT_SIZE} chars"
      fi
      if [[ "$SHOULD_DEFAULT_TOOLLESS" == "true" ]]; then
        TOOLLESS_MODE=true
        log "Using tool-less mode with pre-computed snapshot (${SNAPSHOT_SIZE} chars)"
        # Snapshot mode avoids extra exploration work, so shorter bounds are enough.
        if [[ "$DEEP_TIER" == "large" || "$DEEP_TIER" == "xlarge" ]]; then
          CLAUDE_DEEP_TIMEOUT=300
          CLAUDE_DEEP_RETRY_TIMEOUT=0
        else
          CLAUDE_DEEP_TIMEOUT=240
          CLAUDE_DEEP_RETRY_TIMEOUT=480
        fi
      fi
    else
      warn "Snapshot generation failed; falling back to tool-enabled mode"
    fi
  fi
  CLAUDE_PROMPT="$(build_deep_scan_prompt "$WORKSPACE" "$PROJECT_NAME" "$DEEP_TIER" "$GITIGNORE_CONTEXT" "$REPO_SNAPSHOT")"
  if [[ "$DEEP_TIER" == "large" || "$DEEP_TIER" == "xlarge" ]] && [[ "$TOOLLESS_MODE" == "false" ]]; then
    log "Using fast architecture-first deep scan profile for ${DEEP_TIER} repo (tool-enabled fallback)"
  fi

  DEEP_SCAN_LOG_DIR="$OPENCLAW_DIR/logs"
  mkdir -p "$DEEP_SCAN_LOG_DIR"
  CLAUDE_OUTPUT=""
  CLAUDE_LAST_RC=0
  CLAUDE_LAST_ELAPSED=0
  CLAUDE_LAST_OUT_LOG=""
  CLAUDE_LAST_ERR_LOG=""

  run_claude_deep_scan_attempt() {
    local attempt="$1"
    local timeout_s="${2:-}"
    local out_log="$DEEP_SCAN_LOG_DIR/claude-deep-scan.attempt${attempt}.stdout.log"
    local err_log="$DEEP_SCAN_LOG_DIR/claude-deep-scan.attempt${attempt}.stderr.log"
    local meta_log="$DEEP_SCAN_LOG_DIR/claude-deep-scan.attempt${attempt}.meta.log"
    local rc elapsed start_ts

    : > "$out_log"
    : > "$err_log"
    start_ts="$(date -Is 2>/dev/null || date)"
    {
      echo "attempt=$attempt"
      echo "status=running"
      echo "started_at=$start_ts"
      echo "timeout_s=${timeout_s:-none}"
      echo "toolless_mode=$TOOLLESS_MODE"
      echo "stdout_log=$out_log"
      echo "stderr_log=$err_log"
    } > "$meta_log"

    SECONDS=0
    set +e
    if [[ "$TOOLLESS_MODE" == "true" ]]; then
      # Tool-less mode: pipe prompt via stdin to handle large snapshots safely
      if [[ "$DEEP_USE_TIMEOUT" == "true" ]] && [[ -n "$timeout_s" ]]; then
        printf '%s' "$CLAUDE_PROMPT" | timeout -k 20s "${timeout_s}s" claude --print --tools "" >"$out_log" 2>"$err_log"
        rc=$?
      else
        printf '%s' "$CLAUDE_PROMPT" | claude --print --tools "" >"$out_log" 2>"$err_log"
        rc=$?
      fi
    else
      if [[ "$DEEP_USE_TIMEOUT" == "true" ]] && [[ -n "$timeout_s" ]]; then
        timeout -k 20s "${timeout_s}s" claude --print "$CLAUDE_PROMPT" >"$out_log" 2>"$err_log"
        rc=$?
      else
        claude --print "$CLAUDE_PROMPT" >"$out_log" 2>"$err_log"
        rc=$?
      fi
    fi
    set -e
    elapsed=$SECONDS

    CLAUDE_LAST_RC="$rc"
    CLAUDE_LAST_ELAPSED="$elapsed"
    CLAUDE_LAST_OUT_LOG="$out_log"
    CLAUDE_LAST_ERR_LOG="$err_log"
    CLAUDE_OUTPUT="$(cat "$out_log" 2>/dev/null || true)"

    if [[ -z "$CLAUDE_OUTPUT" ]]; then
      local recovered_output=""
      recovered_output="$(recover_project_output_from_file "$PROJECT_FILE" "$PROJECT_FILE_PRE_SHA" || true)"
      if [[ -n "$recovered_output" ]]; then
        CLAUDE_OUTPUT="$recovered_output"
        rc=0
        CLAUDE_LAST_RC=0
        warn "Detected PROJECT.md written directly during deep scan attempt ${attempt}; preserving generated file"
      fi
    fi

    {
      echo "attempt=$attempt"
      echo "status=finished"
      echo "started_at=$start_ts"
      echo "timeout_s=${timeout_s:-none}"
      echo "rc=$rc"
      echo "elapsed_s=$elapsed"
      echo "toolless_mode=$TOOLLESS_MODE"
      echo "stdout_log=$out_log"
      echo "stderr_log=$err_log"
    } > "$meta_log"

    if [[ "$DEBUG" == "true" ]]; then
      echo "    [DEBUG] Deep scan attempt ${attempt}: rc=${rc}, elapsed=${elapsed}s"
      echo "    [DEBUG] Logs: $out_log | $err_log"
      if [[ -s "$err_log" ]]; then
        echo "    [DEBUG] stderr tail (attempt ${attempt}):"
        tail -40 "$err_log" | sed 's/^/      /'
      fi
      if [[ -z "$CLAUDE_OUTPUT" && -s "$out_log" ]]; then
        echo "    [DEBUG] stdout tail (attempt ${attempt}):"
        tail -20 "$out_log" | sed 's/^/      /'
      fi
    fi
  }

  describe_deep_scan_failure() {
    local rc="$1"
    local elapsed="$2"
    if [[ "$rc" == "124" ]]; then
      echo "timed out after ${elapsed}s (timeout exit 124)"
    elif [[ "$rc" == "0" ]]; then
      echo "returned empty output (rc=0, ${elapsed}s)"
    else
      echo "failed with rc=${rc} after ${elapsed}s"
    fi
  }

  # Run Claude from the target workspace directory
  cd "$WORKSPACE"

  if [[ "$DEEP_USE_TIMEOUT" == "true" ]]; then
    run_claude_deep_scan_attempt 1 "$CLAUDE_DEEP_TIMEOUT"
    if [[ -z "$CLAUDE_OUTPUT" ]] && [[ "$CLAUDE_DEEP_RETRY_TIMEOUT" -gt 0 ]] && [[ "$CLAUDE_DEEP_RETRY_TIMEOUT" -gt "$CLAUDE_DEEP_TIMEOUT" ]]; then
      if [[ "$CLAUDE_LAST_RC" == "137" ]] && [[ "$TOOLLESS_MODE" == "false" ]]; then
        warn "Attempt 1 was killed (rc=137). Switching retry to tool-less snapshot mode."
        REPO_SNAPSHOT="$(build_repo_snapshot "$WORKSPACE" 2>/dev/null || true)"
        if [[ -n "$REPO_SNAPSHOT" ]]; then
          TOOLLESS_MODE=true
          SNAPSHOT_SIZE="${#REPO_SNAPSHOT}"
          CLAUDE_PROMPT="$(build_deep_scan_prompt "$WORKSPACE" "$PROJECT_NAME" "$DEEP_TIER" "$GITIGNORE_CONTEXT" "$REPO_SNAPSHOT")"
          log "Retry will use tool-less mode with pre-computed snapshot (${SNAPSHOT_SIZE} chars)"
        else
          warn "Snapshot generation failed; retrying in tool-enabled mode"
        fi
      fi
      warn "Claude deep scan attempt 1 $(describe_deep_scan_failure "$CLAUDE_LAST_RC" "$CLAUDE_LAST_ELAPSED")"
      warn "Attempt 1 logs: $CLAUDE_LAST_OUT_LOG ; $CLAUDE_LAST_ERR_LOG"
      log "Retrying Claude deep scan once with ${CLAUDE_DEEP_RETRY_TIMEOUT}s..."
      run_claude_deep_scan_attempt 2 "$CLAUDE_DEEP_RETRY_TIMEOUT"
    fi
  else
    run_claude_deep_scan_attempt 1 ""
  fi

  if [[ "$CLAUDE_OUTPUT" == *"The write requires your approval"* ]] || [[ "$CLAUDE_OUTPUT" == *"Please approve the write to proceed"* ]]; then
    warn "Claude returned a write-approval prompt instead of PROJECT.md content — treating as failed deep scan"
    CLAUDE_OUTPUT=""
  fi

  if [[ -n "$CLAUDE_OUTPUT" ]]; then
    CLAUDE_OUTPUT="$(printf '%s' "$CLAUDE_OUTPUT" | sanitize_project_output)"
  fi

  if [[ -n "$CLAUDE_OUTPUT" ]]; then
    echo "$CLAUDE_OUTPUT" > "$PROJECT_FILE"
    log "Created: $PROJECT_FILE"
  else
    warn "Claude deep scan $(describe_deep_scan_failure "$CLAUDE_LAST_RC" "$CLAUDE_LAST_ELAPSED") — creating placeholder"
    warn "Deep scan logs: $CLAUDE_LAST_OUT_LOG ; $CLAUDE_LAST_ERR_LOG"
    {
      echo "$AUTO_GEN_MARKER"
      echo "# PROJECT.md - $PROJECT_NAME Architecture"
      echo ""
      echo "<!-- Deep scan failed or returned empty. Re-run: bash analyze_repo.sh $WORKSPACE --deep -->"
      echo "<!-- Last failure: rc=${CLAUDE_LAST_RC}, elapsed=${CLAUDE_LAST_ELAPSED}s -->"
      echo "<!-- Logs: ${CLAUDE_LAST_OUT_LOG} ; ${CLAUDE_LAST_ERR_LOG} -->"
      echo ""
      echo "## Overview"
      echo ""
      echo "Deep scan did not complete. Populate this file manually or re-run deep scan with a longer timeout:"
      echo ""
      echo "\`CLAUDE_DEEP_TIMEOUT=900 CLAUDE_DEEP_RETRY_TIMEOUT=1800 bash $0 $WORKSPACE --deep\`"
      echo ""
      echo "## Architecture"
      echo ""
      echo "- Pending deep scan output."
      echo "- Start from README.md, primary entrypoints, and top-level build files."
    } > "$PROJECT_FILE"
    if [[ ! -s "$PROJECT_FILE" ]]; then
      cat > "$PROJECT_FILE" <<EOF
$AUTO_GEN_MARKER
# PROJECT.md - $PROJECT_NAME Architecture

Deep scan failed and placeholder generation did not complete.
Re-run with:
\`CLAUDE_DEEP_TIMEOUT=900 CLAUDE_DEEP_RETRY_TIMEOUT=1800 bash $0 $WORKSPACE --deep\`
EOF
    fi
    log "Created placeholder: $PROJECT_FILE"
  fi

  log "Phase B complete."
fi
