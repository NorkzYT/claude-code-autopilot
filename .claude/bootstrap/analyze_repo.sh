set -euo pipefail

# ─────────────────────────────────────────────────────────────
# analyze_repo.sh — Dynamic repo understanding for OpenClaw agents
#
# Usage:
#   bash analyze_repo.sh <workspace-path> [--deep]
#
# Phase A (default): Static parsing of config files
#   Outputs: TOOLS.md, HEARTBEAT.md
#
# Phase B (--deep): Claude-powered deep codebase scan
#   Outputs: PROJECT.md
# ─────────────────────────────────────────────────────────────

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# ─── Helpers ────────────────────────────────────────────────
log()  { printf "\n==> %s\n" "$*"; }
warn() { printf "\n[WARN] %s\n" "$*" >&2; }
skip() { printf "    [SKIP] %s\n" "$*"; }
has()  { command -v "$1" >/dev/null 2>&1; }

node_script_cmd() {
  local pm="$1"
  local script="$2"
  case "$pm" in
    pnpm) echo "pnpm run $script" ;;
    yarn) echo "yarn $script" ;;
    bun)  echo "bun run $script" ;;
    *)    echo "npm run $script" ;;
  esac
}

first_detected_port() {
  if [[ -n "${SERVICES:-}" ]]; then
    printf '%s\n' "$SERVICES" | awk -F'|' 'NF>=2 && $2 ~ /^[0-9]+$/ {print $2; exit}'
  fi
}

AUTO_GEN_MARKER="<!-- AUTO-GENERATED by analyze_repo.sh -->"

# Check if file has custom (non-auto-generated) content
is_custom() {
  local file="$1"
  [[ ! -f "$file" ]] && return 1
  # If file exists but does not have our marker, it's custom
  if ! grep -q "AUTO-GENERATED by analyze_repo.sh" "$file" 2>/dev/null; then
    return 0
  fi
  return 1
}

# Claude sometimes returns explanatory preambles even when asked for markdown-only
# output. Keep only the actual PROJECT.md body before writing the file.
sanitize_project_output() {
  # Use process substitution for the script so stdin remains available for pipe data.
  # A heredoc (<<'PY') would steal stdin from the pipe, making sys.stdin.read() empty.
  python3 <(cat <<'PY'
import sys

marker = sys.argv[1]
text = sys.stdin.read()
if not text:
    sys.exit(0)

text = text.replace("\r\n", "\n")

def trim_to_anchor(s: str) -> str:
    for anchor in (marker, "# PROJECT.md -", "# PROJECT.md"):
        idx = s.find(anchor)
        if idx >= 0:
            return s[idx:]
    return s

def strip_code_fence_wrapper(s: str) -> str:
    lines = s.strip().splitlines()
    if not lines:
        return s.strip()
    # Remove a leading markdown/code fence line if present.
    if lines[0].strip().startswith("```"):
        lines = lines[1:]
    # Remove trailing code fence if present.
    while lines and lines[-1].strip() == "":
        lines.pop()
    if lines and lines[-1].strip().startswith("```"):
        lines.pop()
    return "\n".join(lines).strip()

clean = trim_to_anchor(text)
clean = strip_code_fence_wrapper(clean)

sys.stdout.write(clean)
PY
) "$AUTO_GEN_MARKER"
}

file_sha256() {
  local file="$1"
  if [[ ! -f "$file" ]]; then
    return 1
  fi
  sha256sum "$file" 2>/dev/null | awk '{print $1}'
}

project_file_is_placeholder() {
  local file="$1"
  [[ -f "$file" ]] || return 1
  grep -q "Deep scan did not complete\\|Pending deep scan output\\|Deep scan failed and placeholder generation did not complete" "$file" 2>/dev/null
}

recover_project_output_from_file() {
  local file="$1"
  local previous_sha="${2:-}"
  local current_sha=""
  local recovered=""

  [[ -s "$file" ]] || return 1
  current_sha="$(file_sha256 "$file" || true)"
  [[ -n "$current_sha" ]] || return 1
  [[ "$current_sha" != "$previous_sha" ]] || return 1
  project_file_is_placeholder "$file" && return 1

  recovered="$(cat "$file" 2>/dev/null || true)"
  [[ -n "$recovered" ]] || return 1
  recovered="$(printf '%s' "$recovered" | sanitize_project_output)"
  [[ -n "$recovered" ]] || return 1

  printf '%s' "$recovered"
}

estimate_repo_deep_scan_size() {
  local workspace="$1"

  # Prefer git-aware sizing so .gitignore and exclude-standard rules are honored.
  if has git && git -C "$workspace" rev-parse --is-inside-work-tree >/dev/null 2>&1; then
    python3 - "$workspace" <<'PY'
from pathlib import Path
import subprocess
import sys

root = Path(sys.argv[1])
file_count = 0
total_bytes = 0

cmd = ["git", "-C", str(root), "ls-files", "-z", "--cached", "--others", "--exclude-standard"]
try:
    out = subprocess.check_output(cmd, stderr=subprocess.DEVNULL)
except Exception:
    print("0|0|git-failed")
    raise SystemExit(0)

for rel in out.split(b"\0"):
    if not rel:
        continue
    try:
        path = root / rel.decode("utf-8", "replace")
    except Exception:
        continue
    if not path.is_file():
        continue
    file_count += 1
    try:
        total_bytes += path.stat().st_size
    except OSError:
        pass

print(f"{file_count}|{total_bytes}|git")
PY
    return 0
  fi

  python3 - "$workspace" <<'PY'
from pathlib import Path
import sys

root = Path(sys.argv[1])
exclude = {
    ".git", "node_modules", ".next", ".cache", ".turbo", "dist", "build",
    "__pycache__", ".venv", "venv", "target", "vendor", ".idea", ".vscode"
}

file_count = 0
total_bytes = 0

def walk(p: Path):
    global file_count, total_bytes
    try:
        for child in p.iterdir():
            name = child.name
            if child.is_dir():
                if name in exclude:
                    continue
                walk(child)
            elif child.is_file():
                file_count += 1
                try:
                    total_bytes += child.stat().st_size
                except OSError:
                    pass
    except OSError:
        pass

walk(root)
print(f"{file_count}|{total_bytes}|walk")
PY
}

gitignore_prompt_context() {
  local workspace="$1"
  local gitignore_file="$workspace/.gitignore"
  [[ -f "$gitignore_file" ]] || return 0

  python3 - "$gitignore_file" <<'PY'
from pathlib import Path
import sys

p = Path(sys.argv[1])
try:
    text = p.read_text(errors="replace")
except Exception:
    sys.exit(0)

# Keep this bounded so it doesn't blow up prompt size.
max_chars = 6000
text = text[:max_chars]
text = text.strip()
if not text:
    sys.exit(0)

print("Git ignore rules (read first and honor these; do not inspect ignored paths/files):")
print("```gitignore")
print(text)
print("```")
PY
}

